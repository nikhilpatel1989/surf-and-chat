package nl.tomvanzummeren.msnapi.messenger.service.events.contactlist;

import net.sf.jml.MsnContact;
import nl.tomvanzummeren.msnapi.messenger.service.events.MsnEvent;

/**
 * @author Tom van Zummeren
 */
public class ContactUpdatedEvent implements MsnEvent {

    private MsnContact updatedContact;

    public ContactUpdatedEvent(MsnContact updatedContact) {
        this.updatedContact = updatedContact;
    }

    public MsnContact getUpdatedContact() {
        return updatedContact;
    }
}
