package nl.tomvanzummeren.msnapi.messenger.service.events.contactlist;

import net.sf.jml.MsnContactList;
import nl.tomvanzummeren.msnapi.messenger.service.events.MsnEvent;

/**
 * @author Tom van Zummeren
 */
public class ContactListEvent implements MsnEvent {

    private final MsnContactList contactList;

    public ContactListEvent(MsnContactList contactList) {
        this.contactList = contactList;
    }

    public MsnContactList getContactList() {
        return contactList;
    }
}
